# Set datatype method
# add method
s1={'google','apple'}
s1.add('microsoft')
print(s1)       # {'google', 'microsoft', 'apple'}

#-----------------------------------------------------------------------------------------------------------------------------#

# remove method
s1={'google','apple','microsoft'}
s1.remove('apple')
print(s1)       # {'google', 'microsoft'}

#-----------------------------------------------------------------------------------------------------------------------------#

# update method
s1={'google','microsoft'}
s2={'apple',123}
s1.update(s2)
print(s1)       # {'google', 'microsoft', 123, 'apple'}

#-----------------------------------------------------------------------------------------------------------------------------#

# pop method
s1={'google', 'microsoft', 123, 'apple'}
s1.pop()
print(s1)       # {'microsoft', 123, 'apple'}

#-----------------------------------------------------------------------------------------------------------------------------#

# clear method
s1={'microsoft', 123, 'apple'}
s1.clear()
print(s1)   # set()

#-----------------------------------------------------------------------------------------------------------------------------#

# union method
s1={1,2,3,4}
s2={3,4,5,6}
s1.union(s2)        # {1, 2, 3, 4, 5, 6}

#-----------------------------------------------------------------------------------------------------------------------------#

# intersection method
s1={1,2,3,4}
s2={3,4,5,6}
s1.intersection(s2)     #{3,4}

#-----------------------------------------------------------------------------------------------------------------------------#

# differnce method
s1={1,2,3,4}
s2={3,4,5,6}
s1.difference(s2)       # {1,2}

#-----------------------------------------------------------------------------------------------------------------------------#

# symmetric difference
s1={1,2,3,4}
s2={3,4,5,6}
s1.symmetric_difference(s2)         # {1,2,5,6}

